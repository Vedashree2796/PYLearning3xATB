# Ternary operator
Veda_marks = 80
Priya_marks = 85
# x > y -> do something - print("veda)
# y > x _> do something else -> print ("Priya")
print("Veda is winner" if Veda_marks > Priya_marks else "Priya is winner")


if Veda_marks > Priya_marks:
     print("Veda is the winner")
else:
    print("Priya is the winner")
